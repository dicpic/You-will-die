//не докуривать чужие бычки(то же самое, что и предыдущий пункт); 
//не трепать лишнего(таких не любят); не жеманничать и не строить из себя интеллигента(можно попасть в разряд опущенных); 
//не трогать никакие чужие вещи без ведома и разрешения владельца;
//не садиться на парашу по большой нужде, если кто - либо кушает; 
//не плевать в хате, не портить воздух.
//не Расспрашивать заключённых об их делах на воле, за что сидят, в общем, не лезть в душу. Это не принято.
//не Садиться играть в карты или другие азартные игры, даже если предлагаю играть «на просто так». 
