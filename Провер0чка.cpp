//-Приветствие
//-хата мужитская или гопарская 
//-«За что осуждён ? » или «За что попал ? » – истинная статья, согласно которой новичок прибыл в колонию.
//-Могут спросить о сроке, получивший по суду. - Коротко ответить.
//-«Ты кто ? ». Вопрос означает, кем вновь прибывший себя считает, учитывая конечно, статью и свою «чистоту». Ответ должен быть 
//правдивый. Если новичок нигде себя не замарал, а статья вполне приличная(без изнасилований и растления малолетних или других 
//преступлений, не особенно почитаемых в тюрьме), то можно ответить «мужик». Вор, не впервые оказавшийся за решёткой, обычно отвечает 
//«бродяга», а опущенный – «опущенный человек».
//-«Какое погоняло?». Нужно сказать кличку, а если её не было отродясь, то придумать что-либо подходящее. Иначе кликуху дадут сами 
//зэки, но она не всегда может понравиться. Об этом тоже лучше подумать заранее.
//-Испытание полотенцем - Мало уже осталось зон, где вновь прибывшему бросают на входе полотенце под ноги, чтобы узнать, как он поступит. Это своего рода 
//испытание, выясняющее, знаком ли человек с зоновскими неписаными традициями, законами и правилами.
//-Прописка - Если в первый раз первоход не выдержит испытание, то ему дают возможность исправить ситуацию в следующий раз. Но если и во второй 
//раз он не пройдёт, то ему дают выбор : присоединиться к опущенным или к чухоням.Можно избежать такого выбора выкупом или отработкой.
//Вот некоторые вещи, о которых нужно помнить всегда : никогда не поднимать чужие вещи(можно стать чьим - то слугой); 
